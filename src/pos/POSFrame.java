    private void jButton22ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton22ActionPerformed
        // TODO add your handling code here:
        //check if entry exists in the db
        int wantedRow = 0;
        String barCode = jTextField20.getText();
        int barCodeNumber = Integer.parseInt(barCode);
        String [] barCodeArray = myDatabase.getResultSet(0, "inventoryIds");
        Boolean found = false;
        Boolean barCodeFound = false;
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        
        //loop through result and check if it exists
        for(String code: barCodeArray) {
            if(barCode.equals(code)) {
                found = true;
            } //end of if
        } //end of for loop
        
        //if the barcode exists in the system
        if(found) {
            //store the details to add to the table
            String[] displayOutput = myDatabase.getResultSet(barCodeNumber, "inventoryIds");
            String itemNumber = displayOutput[0];
            String name = displayOutput[1];
            double price = Double.parseDouble(displayOutput[3]);
            double total = 0.0;
            
            //check if it exists in the table
            int rowCounter = model.getRowCount();
            if(rowCounter == 0) {
                barCodeFound = false;
            }
            else {
               for(int tempCounter = 0; tempCounter < rowCounter; tempCounter++) {
                   int tempBarCode = Integer.parseInt((String) model.getValueAt(tempCounter, 1));
                   if(tempBarCode == barCodeNumber) {
                       barCodeFound = true;
                       wantedRow = tempCounter;
                    } //end of if
                } //end of for 
            } //end of else
            
            //if the bar code was found in the table
            if(barCodeFound) {
                double tempItemPrice = Double.parseDouble(displayOutput[3]);
                
                //get the current quantity and modify it
                int currentQuantity = (int) model.getValueAt(wantedRow, 3);
                currentQuantity++;
                model.setValueAt((Object) currentQuantity, wantedRow, 3);
                
                //get the current amount and modify it
                double currentAmount = (double) model.getValueAt(wantedRow, 5);
                currentAmount += tempItemPrice;
                model.setValueAt((Object) (currentAmount), wantedRow, 5);
            } //end of if
            
            //if the bar code was not found in the table (add it)
            else {
                ++tableCounter;
                model.addRow(new Object [] {tableCounter, itemNumber, name, 1,
                price, price});
            } //end of else
            
            //display the total on the label
            //loop to get total
            int amountRowCounter = model.getRowCount();
            for(int tempCounter = 0; tempCounter < amountRowCounter; tempCounter++) {
                double amount = (double) model.getValueAt(tempCounter, 5);
                total += amount;
            } //end of for
            
            //set the total to the jLabel
            jLabel58.setText(Double.toString(total));
            //set textfield to empty for next bar code
            jTextField20.setText("");
            
        } //end of if
        else {
            JOptionPane.showMessageDialog(rootPane, "The entered bar code does not exist in the system.", 
                    "Non-existent bar code", JOptionPane.INFORMATION_MESSAGE);
            jTextField20.setText("");
        } //end of final else
    }//GEN-LAST:event_jButton22ActionPerformed

    private void jButton25ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton25ActionPerformed
        // TODO add your handling code here:
        //generate the receipt
        
        //declaration of necessary variables
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        
        String userName = jLabel56.getText();
        String [] displayOutput = myDatabase.getResultSet(0, userName);
        ArrayList< String > itemsList = new ArrayList< String >();
        ArrayList< Integer > quantitiesList = new ArrayList< Integer >();
        String [] items;
        int [] quantities = new int[model.getRowCount()];
        double [] prices = new double[model.getRowCount()];
        double [] amounts = new double[model.getRowCount()];
        double total = Double.parseDouble(jLabel58.getText());
        double cash = Double.parseDouble(jLabel60.getText());
        double change = Double.parseDouble(jLabel62.getText());
        double vat = 0.16;
        double vatableAmt = total / (1 + vat);
        
        int rowCounter;
        
        //loop to get all the items
        rowCounter = model.getRowCount();
        
        if(rowCounter != 0) {
            //loop to get all the necessary values from the jTable
            for(int tempCounter = 0; tempCounter < rowCounter; tempCounter++) {
                itemsList.add((String) model.getValueAt(tempCounter, 2));
                quantitiesList.add((int) model.getValueAt(tempCounter, 3));
                quantities[tempCounter] = (int) model.getValueAt(tempCounter, 3);
                prices[tempCounter] = (double) model.getValueAt(tempCounter, 4);
                amounts[tempCounter] = (double) model.getValueAt(tempCounter, 5);
                //pricesList.add((String) model.getValueAt(tempCounter, 4));
                //amountsList.add((String) model.getValueAt(tempCounter, 5));
            } //end of for
            
            //convet arrayLists to arrays
            items = (String []) itemsList.toArray(new String[itemsList.size()]);
            //quantities = (int [])quantitiesList.toArray(new Integer[quantitiesList.size()]);
            
            //call method to print receipt
            myPrint.generateReceipt(userName, items, quantities, prices, amounts, total, cash, change, vat);
            
            //call method to add transaction to the table
            int empId = Integer.parseInt(displayOutput[0]);
            myTransaction.addTransaction(empId, total);
            
            //loop to delete the rows
            for(int tempCounter = rowCounter - 1; tempCounter >= 0; tempCounter--) {
                model.removeRow(tempCounter);
            } //end of for
                    
            //display message and reset necessary fields
            JOptionPane.showMessageDialog(null, "The receipt has successfully been printed.\nResetting fields for the next customer.",
                "All Done", JOptionPane.INFORMATION_MESSAGE);
                    
            jLabel58.setText("0.00");
            jLabel60.setText("Waiting...");
            jLabel62.setText("Waiting...");
            jLabel96.setText("Waiting...");
            
            
                    
        }
        else {
            JOptionPane.showMessageDialog(rootPane, "The table seems to be empty."
                    + "\nEnsure you have entered items first.", "No data", JOptionPane.INFORMATION_MESSAGE);
        } //end of else
    }//GEN-LAST:event_jButton25ActionPerformed

